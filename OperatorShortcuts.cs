using System;

namespace MakingProgress
{
  class Program
  {
    static void Main(string[] args)
    {
      // Declare steps variable
      int steps = 0;


      // Two steps forward 
      steps += 2;
      Console.WriteLine(steps);

      // One step back 
      steps -= 1;
      Console.WriteLine(steps);
      // Print result to the console

    }
  }
}
